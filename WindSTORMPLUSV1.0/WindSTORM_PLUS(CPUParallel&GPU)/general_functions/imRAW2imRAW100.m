function imRAW100 = imRAW2imRAW100(imRAW,imW,imH,imF)
% ------------------------------------------------------------------------------------
% 并行计算时的图像分割
% 
% By Wen Xiao @SZU
%------------------------------------------------------------------------------------
%在第四维度分割成100的子集，方便parfor做并行计算，互不干扰
seg = floor(imF/100);
imRAW100 = single(zeros(imH,imW,100,seg));
for i=1:seg
    imRAW100(:,:,:,i) = imRAW(:,:,(i-1)*100+1:i*100);
end
end